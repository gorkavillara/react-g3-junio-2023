/**
 * La función multiplica devuelve el producto de los dos parámetros
 * @param a El primer factor
 * @param b El segundo factor
 * @returns El producto de la multiplicación de a * b
 */
const multiplica = (a: number, b: number): number => a * b

/**
 * Devuelve el número de caracteres del texto pasado por parámetros
 * @param texto El texto del cual queremos obtener el número de caracteres
 * @returns El número de caracteres del texto pasado por parámetros
 */
const obtenNumeroCaracteres = (texto: string): number => texto.length

export { multiplica, obtenNumeroCaracteres }
